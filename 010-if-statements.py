# If statements -- a basic form of decision making based upon conditions

# If it's a hot day
#   Drink lots of water
# otherwise
#   Bring a jacket with you
# or else
#   It is a lovely day

is_hot = True
is_cold = False

if is_hot:
    print('Drink a lot of water! It is hot!')
elif is_cold:
    print('It is cold! Bring a jacket with you!')    
else:
    print('It is a lovely day')

print('Enjoy your day!')