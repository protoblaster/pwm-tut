import math

url = 'https://docs.python.org/3/library/math.html'

x = 2.9
y = -2.9

print('round(2.9):', round(x))
print('abs(-2.9):', abs(y))    # 'abs' means absolute (how far the number is away from 0)

pi = math.pi

print('PI:', pi)
print('math.ceil(2.9):', math.ceil(x))
print('math.floor(2.9):', math.floor(x))

