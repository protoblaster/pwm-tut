# comparison operators -- used to compare 2 or more values
#
# < -- less than
# > -- greater than
# <= -- less than or equal to
# >= -- greater than or equal to
# == -- equal to

temperature = 31

if temperature > 30:
    print('It is a hot day')
elif temperature < 10:
    print('It is a cold day')
else:
    print('It is neither hot nor cold')

